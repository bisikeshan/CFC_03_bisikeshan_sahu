// Header
#include <stdio.h>

/* 
    This program checks if a person is a senior citizen. If the age is 60 or more, they are a senior citizen */

// Entry point function
int main(void)
{
	 int age = 61; 
	 if(age >= 60)
	 {
	 	printf("you are a senior citizen.\n");
	 }
	 else
	 {
	 	printf("you are not a senior citizen.\n");
	 }

	 return 0; 
}
