// program to print reverse countinng from 1 to 10 using while loop  

// Headers
#include <stdio.h>

// Entry point function 
int main()
{
	  // iniialization 
	  int le = 10; 

	  // termination condition 
	  while(le >= 1)
	  {
	  	 // loop block 
	  	printf("%d\n",le);

	  	// increment / decrement
	  	le--; 
	  }

	  return 0; 
}